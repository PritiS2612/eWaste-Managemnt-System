<h2 style="font-family: fantasy; font-size: 30px; font-weight: bold; color: seagreen;">Extended Producer Responsibility – E-Waste Management Rules, 2016</h2>
<p style="color: grey;">India has one of the fastest growing electronic consumer markets in the world. Consequently, India also has a high level of e-waste being generated when these products reach the end of their use. Recognising the toxic and hazardous effects of the lack of this e-waste management on the environment, the Government of India has introduced an Extended Producer Responsibility in the E-Waste Management Rules, 2016.</p>

<p style="color: grey;">
  These guidelines iron out the set target of products which the producers have to ensure these are recycled when they reach the end of their use. Failure to do so involves the levy of heavy fines. The guidelines aim to ensure that each stakeholder involved in the mass generation of e-waste is taking steps to ensure its safe disposal. Through these guidelines, we hope to see a model where the product which is at the end of its life is brought back to the producer and thereafter recycled by authorized recyclers.
</p>
<b>Problems faced by the Stakeholders</b>
<p style="color: grey;">
  Though the establishment of such a system is the aim, producers and manufacturers of electronic goods face issues with respect to implementation due to the rate of generation of e-waste, and the lack of infrastructure for the collection and subsequent recycling of e-waste. Producers have been seen to cite the target for recyclable e-waste as high with heavy penalties in cases where guidelines weren’t met. This discomfort for producers is visible and ultimately inevitable.

  </p>
  <b>Our Solution to EPR Guidelines</b>

  <p style="color: grey;">We at Namo are one of the first few organisations to provide Producer Responsibility Organisation (PRO) services to help these producers meet their EPR guidelines. In doing so, we aim to ensure that e-waste producers are able to ensure that waste products are collected from their customers and delivered to our facility for the responsible recycling and disposal of their product. To further ensure convenience, we provide the producer with the requisite recycling certification along with a set of compliance documents which adhere to government standards and guidelines.
  </p>


<button class="accordion">Large Network for Optimal eWaste Collection</button>
<div class="panel">
  <p>We operate through a network of channels to collect e-waste from consumers, aggregators, the informal sector and other sources, on behalf of producers and transport them to our recycling plant in Faridabad. Additionally, we aim to increase awareness on the environmental and personal benefits of e-waste recycling to ensure that the hazardous informal method of recycling reduces and is ultimately eradicated.</p><hr>
</div>

<button class="accordion">Fillers of the Gap</button>
