            <h3>E-Waste (Management) Rules, 2016 - What’s New?</h3>
            <p class="font-italic" style="text-align: justify;  text-justify: inter-word;">
              E- Waste (Management) Rules, 2016 In October 2016, the E-Waste (Management) Rules, 2016 replaced the E-Waste (Management and Handling) Rules, 2016. This set of rules clarifies duties of responsible parties, enacts more stringent regulations on e-waste production, as well as clarifies the general definition of e-waste. In these rules, e-waste is defined as "electrical and electronic equipment, whole or in part discarded as waste by the consumer or bulk consumer as well as rejects from manufacturing, refurbishment and repair processes. ‘Electrical and electronic equipment’ in turn has been defined to mean equipment which are dependent on electric current or electro-magnetic field in order to become functional."
            </p>
            <ul style="text-align: justify;  text-justify: inter-word;">
              <li><i class="icofont-check"></i> Manufacturer, dealer, refurbisher and Producer Responsibility Organization (PRO) have been introduced as additional stakeholders in the rules. </li>
              <li><i class="icofont-check"></i> The applicability of the rules has been extended to components, consumables, spares and parts of EEE in addition to equipment as listed in Schedule I. </li>
              <li><i class="icofont-check"></i> Compact Fluorescent Lamp (CFL) and other mercury containing lamp brought under the purview of rules. </li>
              <li><i class="icofont-check"></i> Collection mechanism based approach has been adopted to include collection centre, collection point, take back system etc for collection of e - waste by Producers under Extended Producer Responsibility (EPR). </li>
              <li><i class="icofont-check"></i> Provision for Pan India EPR Authorization by CPCB has been introduced replacing the state wise EPR authorization. </li>
              <li><i class="icofont-check"></i>  Dealer or retailer or e - retailer shall refund the amount as per take back system or De posit Refund Scheme of the producer to the depositor of e - waste. </li>
              <li><i class="icofont-check"></i> The transportation of e - waste shall be carried out as per the manifest system whereby the transporter shall be required to carry a document (three copies) prepared by the sender, giving the details. </li>
              <li><i class="icofont-check"></i> State Government to prepare integrated plan for effective implementation of these provisions, and to submit annual report to Ministry of Environment, Forest and Climate Change. </li>
              <li><i class="icofont-check"></i> Urban Local Bodies (Municipal Committee/Council/Corporation) has been assign the duty to collect and channelized the orphan products to authorized dismantler or recycler.</li>



