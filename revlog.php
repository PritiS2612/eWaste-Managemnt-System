<h1 align="center">Reverse Logistics</h1>
<div class="padd">
<img src="images/revlog.jpg" class="img-fluid" alt="" width="1000" height="500"><br/><br/>

<p style="color: grey;">One of the biggest factors that are preventing Producers from meeting their Extended Producer Responsibility Guidelines (EPR) and facilitate the recycling of their product, is the logistical cost of collection, transportation, and thereafter its reuse and responsible disposal. This cost is exorbitant, due to the varying geographical terrain of our country.</p>

<p style="color: grey;">
 To facilitate producers and address this issue, we have established a widespread presence across India to deliver a customized Reverse Logistics service which collects e-waste from the customer’s doorstep and transports it directly to our facility. We ensure this collection within 96 hours of scheduling the pick-up and ensure that both hazardous and non-hazardous e-waste is transported safely, securely and responsibly, through our channels.
</p>
<button class="accordion">Advanced Mechanism</button>
<div class="panel">
  <p>We have established an advanced Reverse Logistics mechanism to bring all our customer’s e-waste to our factory in secured transportation with the help of our strong local network, wide presence and channel partners. We deploy our own fleet of vehicles to prevent any inefficiencies and ensure the safe and sustainable disposal of e-waste.

</p><hr>
</div>

<button class="accordion">24*7 Support and Logistic Partners</button>
<div class="panel">
